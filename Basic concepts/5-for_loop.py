# for loop range 1 to 10
for i in range(10):
    print(i)
   


# for loop start from index 2
for x in range(2, 6):
  print(x)



# for with increament of more then 1
for y in range(1,20,3):
    print(y)



# Nested for loop
for a in range(2):
    for b in range(2):
        print(a,"  ",b)



# else in for loop
for j in range(4):
    print(j)
else:
    print('Program end')
    
   

# Reverse for loop
# Method 1
for i in range(9,-1,-1):
    print(i)
    
    
#Method 2 
for i in reversed(range(10)):
    print(i)
