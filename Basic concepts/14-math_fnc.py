import math

a=5
# Square
print(a**2)

# Factorial
fac=math.factorial(a)
print(fac)

# remainder
rem= math.remainder(a,4)
print(rem)

# cos 
cos= math.cos(a)
print(cos)

# sin
sin= math.sin(a)
print(sin)

# tan
tan= math.tan(a)
print(tan)

# degree
deg= math.degrees(sin)
print(deg)

# radian
rad= math.radians(deg)
print(rad)

# round off values
print(math.ceil(3.9))
